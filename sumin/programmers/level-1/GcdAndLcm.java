class Solution {
    public int[] solution(int n, int m) {
        
		if(n > m) {
			int temp = m;
			m = n;
			n = temp;
		}
        
        int a = n;
        int b = m;
		
		//ìµœëŒ€ê³µì•½ìˆ˜
		while(b != 0){			
			int temp = b;
            b = a % b;
            a = temp;			
		}
		int gcd = a;
		int lcm = (m * n) / gcd;
		
		
        return new int[] {gcd, lcm};
    }
}


